    @SuppressLint("MissingPermission")
    public static String getMac(Context context) {
        String strMac = null;

        if (Build.VERSION.SDK_INT < Build.VERSION_CODES.M) {

            strMac = getLocalMacAddressFromWifiInfo(context).replace(":", "").toUpperCase();
            return strMac;
        } else if (Build.VERSION.SDK_INT < Build.VERSION_CODES.N
                && Build.VERSION.SDK_INT >= Build.VERSION_CODES.M) {

            strMac = getMacAddress(context).replace(":", "").toUpperCase();
            return strMac;
        } else if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.N) {
            Log.e("=====", "7.0以上");
            if (!TextUtils.isEmpty(getMacAddress())) {
                Log.e("=====", "7.0以上1");
                strMac = getMacAddress().replace(":", "").toUpperCase();
                return strMac;
            } else if (!TextUtils.isEmpty(getMachineHardwareAddress())) {
                Log.e("=====", "7.0以上2");
                strMac = getMachineHardwareAddress().replace(":", "").toUpperCase();
                return strMac;
            } else {
                Log.e("=====", "7.0以上3");
                strMac = getLocalMacAddressFromBusybox().replace(":", "").toUpperCase();
                return strMac;
            }
        }

        return "02:00:00:00:00:00";
    }


